public class FinalKeyword {
    public static void main(String args[]){
        final double PI=3.14;
        // PI=3; we can't change the variable
        System.out.println(PI);
    }
}
//Notes*
//we can use the finalkeyword with Variable,Method and class
//1 .Final keyword is used to make the variable constant.
//2.Final Keyword is used to stop the methode overriding
//3. Final keyword is used to stop the inheritance(of class)